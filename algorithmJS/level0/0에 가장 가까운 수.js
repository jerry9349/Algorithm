
/*문제*/
/*
정수 배열 arr가 주어집니다. 이 중 0에서 가장 가까운 값을 구하는 프로그램을 구현하세요.


입력 형식
- arr : 주엊진 정수 배열

출력 형식
- 조건에 맞는 값을 정수로 반환

제약 사항
- 0 < arr.length <= 100
- 0 < arr[i] <= 100



입출력 예시
- 예시1
- 입력 : arr = [5, 2, 1, 3]
- 출력 : 1
- 설명 : 0에 가장 가까운 수 1이 답안이다.






*/



/*
*
*
*
*
*
*/
/* 내 풀이*/
function solution(arr) {
  return Math.min(...arr);
}
