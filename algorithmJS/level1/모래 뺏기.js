/* 내 풀이 */
function canWinSandGame(num) {
    // 흥민이의 승리 여부를 저장하는 배열
    const dp = new Array(num + 1).fill(false);
  
    // 초기 조건 설정
    dp[0] = false; // 0kg의 모래는 이미 쓰러진 상태
    dp[1] = false; // 1kg의 모래는 흥민이의 패배
  
    // 동적 계획법을 활용하여 흥민이의 승리 여부 계산
    for (let i = 2; i <= num; i++) {
      // 가능한 다음 상태들 중에서 패배하는 경우가 있는지 확인
      if (!dp[i - 1] || !dp[i - 2] || !dp[i - 3]) {
        dp[i] = true; // 흥민이의 승리
      } else {
        dp[i] = false; // 흥민이의 패배
      }
    }
  
    return dp[num];
  }
  
  // 예시 실행
  const num = 10; // 시작 모래의 양
  const result = canWinSandGame(num);
  console.log(result); // true 또는 false 출력



